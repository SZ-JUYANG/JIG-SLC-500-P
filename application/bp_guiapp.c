/*********************************************************************
*
*       _ShowDialog
*
* Purpose:
*   Hides the previous dialog, creates or shows the new dialog
*/
static void _ShowDialog(int Number) {
  WM_CALLBACK * afpDialog[GUI_COUNTOF(_aDlgResource)] = { /* Array of callback function pointers */
    _cbDialog_A, _cbDialog_B, _cbDialog_C, _cbDialog_D, _cbDialog_E
  };
  if (_CurrentDialogIndex == Number) {
    return; /* Return if nothing to do */
  }
  if (_hDialog[_CurrentDialogIndex]) {
    WM_HideWindow(_hDialog[_CurrentDialogIndex]); /* Hide old dialog */
  }
  _CurrentDialogIndex = Number;
  if (_hDialog[_CurrentDialogIndex]) {
    WM_ShowWindow(_hDialog[_CurrentDialogIndex]); /* Show new dialog */
  } else {
    /* Create new dialog */
    _hDialog[Number] = GUI_CreateDialogBox(_aDlgResource[_CurrentDialogIndex].pResource, 
                                           _aDlgResource[_CurrentDialogIndex].nEntrys, 
                                           afpDialog[_CurrentDialogIndex], 
                                           _hParent, 0, 0);
  }
}

/*********************************************************************
*
*       _cbButton
*
* Purpose:
*   Callback routine for the transparent buttons A - E
*/
static void _cbButton(WM_MESSAGE *pMsg) {
  char acBuffer[10];
  GUI_RECT Rect;
  WM_HWIN hWin;
  hWin = pMsg->hWin;
  switch (pMsg->MsgId) {
  case WM_PAINT:
    /* Draw a transparent button */
    WM_GetClientRect(&Rect);
    GUI_SetColor(GUI_BLACK);
    GUI_DrawRectEx(&Rect);
    if (BUTTON_IsPressed(hWin)) {
      GUI_DrawRect(1, 1, Rect.x1 - 1, Rect.y1 - 1);
      GUI_SetFont(&GUI_Font24B_ASCII);
    } else {
      GUI_SetFont(&GUI_Font24_ASCII);
    }
    GUI_SetTextMode(GUI_TM_TRANS);
    BUTTON_GetText(hWin, acBuffer, sizeof(acBuffer));
    GUI_DispStringInRect(acBuffer, &Rect, GUI_TA_HCENTER | GUI_TA_VCENTER);
    break;
  default:
    _pcbCallbackButton(pMsg); /* The original callback */
    break;
  }
}

/*********************************************************************
*
*       _cbMenu
*
* Purpose:
*   Callback routine for button menu
*/
static void _cbMenu(WM_MESSAGE * pMsg) {
  WM_HWIN hWin, ahButton[5];
  int i, Id;
  hWin = pMsg->hWin;
  switch (pMsg->MsgId) {
  case WM_CREATE:
    /* Create the buttons A - E */
    for (i = 0; i < GUI_COUNTOF(ahButton); i++) {
      char acButtonText[] = {" "};
      acButtonText[0] = 'A' + i;
      /* Create the buttons with transparency flag */
      ahButton[0] = BUTTON_CreateEx(10, 10 + i * 40, 35, 35, 
                                    hWin, WM_CF_SHOW | WM_CF_HASTRANS, 0, GUI_ID_BUTTON0 + i);
      BUTTON_SetFont(ahButton[0], &GUI_Font24B_ASCII);
      BUTTON_SetText(ahButton[0], acButtonText);
      /* Save the original button callback routine for default operations 
         and set the new callback routine for buttons A - E */
      if (!i) {
        _pcbCallbackButton = WM_SetCallback(ahButton[0], _cbButton);
      } else {
        WM_SetCallback(ahButton[0], _cbButton);
      }
    }
    break;
  case WM_PAINT:
    //GUI_DrawBitmap(&bmBackGround, 0, 0); /* Draw the background */
    break;
  case WM_NOTIFY_PARENT:
    if (pMsg->Data.v == WM_NOTIFICATION_RELEASED) {
      Id = WM_GetId(pMsg->hWinSrc);      /* Id of widget */
      _ShowDialog(Id - GUI_ID_BUTTON0);  /* Show the dialog */
    }
    break;
  default:
    WM_DefaultProc(pMsg);
  }
}

