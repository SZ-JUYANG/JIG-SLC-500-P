/*********************************************************************
*
*       _cbDialog_A
*
* Purpose:
*   Callback routine of dialog A
*/
static void _cbDialog_A(WM_MESSAGE * pMsg) {
  int Id, NCode;
  WM_HWIN hDlg, hItem;
  hDlg = pMsg->hWin;
  switch (pMsg->MsgId) {
  case WM_PAINT:
    /* Owner drawn background of dialog */
    GUI_SetBkColor(GUI_LIGHTGREEN);
    GUI_Clear();
    break;
  case WM_INIT_DIALOG:
    /* Init slider */
    hItem = WM_GetDialogItem(hDlg, GUI_ID_SLIDER0);
    SLIDER_SetRange(hItem, 0, 9999);
    SLIDER_SetValue(hItem, _Value);
    /* Init edit widget */
    hItem = WM_GetDialogItem(hDlg, GUI_ID_EDIT0);
    EDIT_SetDecMode(hItem, _Value, 0, 9999, 0, 0);
    EDIT_SetTextAlign(hItem, GUI_TA_HCENTER);
    break;
  case WM_NOTIFY_PARENT:
    NCode = pMsg->Data.v;               /* Notification code */
    switch (NCode) {
    case WM_NOTIFICATION_RELEASED:      /* React only if released */
      Id = WM_GetId(pMsg->hWinSrc);     /* Id of widget */
      if (Id == GUI_ID_BUTTON0) {
        GUI_EndDialog(hDlg, 1);
        _hDialog[_CurrentDialogIndex] = 0;
        _CurrentDialogIndex = -1;
      }
      break;
    case WM_NOTIFICATION_VALUE_CHANGED: /* Value has changed */
      Id = WM_GetId(pMsg->hWinSrc);     /* Id of widget */
      if (Id == GUI_ID_SLIDER0) {
        /* SLIDER-widget has changed, update EDIT-widget */
        hItem = WM_GetDialogItem(hDlg, GUI_ID_SLIDER0);
        _Value = SLIDER_GetValue(hItem);
        hItem = WM_GetDialogItem(hDlg, GUI_ID_EDIT0);
        EDIT_SetValue(hItem, _Value);
      } else if (Id == GUI_ID_EDIT0) {
        /* If EDIT-widget has changed, update SLIDER-widget */
        hItem = WM_GetDialogItem(hDlg, GUI_ID_EDIT0);
        _Value = EDIT_GetValue(hItem);
        hItem = WM_GetDialogItem(hDlg, GUI_ID_SLIDER0);
        SLIDER_SetValue(hItem, _Value);
      }
      break;
    }
    break;
  default:
    WM_DefaultProc(pMsg);
  }
}

