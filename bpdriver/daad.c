extern u16  AD_Value[ADC_TIMES][ADC_CHANALS];   //用来存放ADC转换结果，也是DMA的目标地址
extern u16  After_filter[ADC_CHANALS];    		 //用来存放求平均值之后的结果


GPIO_InitTypeDef     GPIO_InitStructure;

 void BP_DAC_Init(void)
 {
	  	DAC_InitTypeDef DAC_InitStructure;
 		DAC_DeInit();	  //恢复DAC寄存器到初始值

		GPIO_InitStructure.GPIO_Pin =  GPIO_Pin_4 | GPIO_Pin_5; //DACOUT1为PA4DAC OUT2为PA5
  		GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz; 
  		GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 
  		GPIO_Init(GPIOA, &GPIO_InitStructure); 

		RCC_APB1PeriphClockCmd(RCC_APB1Periph_DAC, ENABLE);//使能DAC时钟 


		DAC_Cmd(DAC_Channel_1,ENABLE); //使能DAC通道1
		DAC_Cmd(DAC_Channel_2, ENABLE);//使能DAC通道2

	    /* 配置DAC通道 */ 
  		DAC_InitStructure.DAC_Trigger = DAC_Trigger_None; //不用触发方式
  		DAC_InitStructure.DAC_WaveGeneration = DAC_WaveGeneration_None;	//不用波形发生器
		DAC_InitStructure.DAC_LFSRUnmask_TriangleAmplitude=DAC_TriangleAmplitude_4095;//三角波发生器输出最大幅度为4095
  		DAC_InitStructure.DAC_OutputBuffer = DAC_OutputBuffer_Enable; //用输出缓存
  		DAC_Init(DAC_Channel_1, &DAC_InitStructure); 	//初始化DAC通道1
		DAC_Init(DAC_Channel_2, &DAC_InitStructure); 	//初始化DAC通道2
 }


  void BP_DAC1_InitTriangle(void)  //将DAC1设置为三角波发生器，使用TIM2做时钟源
 {
	  	DAC_InitTypeDef DAC_InitStructure;
		TIM_TimeBaseInitTypeDef  TIM_TimeBaseStructure;
 		DAC_DeInit();	  //恢复DAC寄存器到初始值

		GPIO_InitStructure.GPIO_Pin =  GPIO_Pin_4 ; //DACOUT1为PA4
  		GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz; 
  		GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 
  		GPIO_Init(GPIOA, &GPIO_InitStructure); 

		RCC_APB1PeriphClockCmd(RCC_APB1Periph_DAC, ENABLE);//使能DAC时钟 
		RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM2, ENABLE); //TIM2时钟使能
		//设置定时器2为三角波提供源
		TIM_TimeBaseStructInit(&TIM_TimeBaseStructure);
		TIM_TimeBaseStructure.TIM_Period=71; //   计数值71+1=72
		TIM_TimeBaseStructure.TIM_Prescaler=4; // 4+1=5分频，频率72/5
		//定时器2溢出频率为5M
		TIM_TimeBaseStructure.TIM_ClockDivision=0x0;
		TIM_TimeBaseStructure.TIM_CounterMode=TIM_CounterMode_Up;
		TIM_TimeBaseInit(TIM2,&TIM_TimeBaseStructure);
		TIM_SelectOutputTrigger(TIM2,TIM_TRGOSource_Update);//选择定时器2为触发输出模式
		
		DAC_InitStructure.DAC_Trigger=DAC_Trigger_T2_TRGO; //配置DAC1触发源为定时器2
		DAC_InitStructure.DAC_WaveGeneration=DAC_WaveGeneration_Triangle; //配置波形发生器为三角波发生器
		DAC_InitStructure.DAC_LFSRUnmask_TriangleAmplitude=DAC_TriangleAmplitude_2047; //最大幅度2047
		DAC_InitStructure.DAC_OutputBuffer=DAC_OutputBuffer_Enable;//输出缓冲使能;
		DAC_Init(DAC_Channel_1, &DAC_InitStructure); //根据DAC_InitStructure的设置配置DAC1
		DAC_Cmd(DAC_Channel_1,ENABLE); //使能DAC通道1
		DAC_SetChannel1Data(DAC_Align_12b_R,0);
		TIM_Cmd(TIM2,ENABLE);//使能定时器输出，将发出三角波
 }
 /*配置DMA*/
void BP_ADC_DMA_Init(void)
{
	/* ADC1  DMA1 Channel Config */
	DMA_InitTypeDef DMA_InitStructure;
	RCC_AHBPeriphClockCmd(RCC_AHBPeriph_DMA1, ENABLE);//使能DMA传输

	DMA_DeInit(DMA1_Channel1);   //将DMA的通道1寄存器重设为缺省值
	DMA_InitStructure.DMA_PeripheralBaseAddr =  (u32)&ADC1->DR;  //DMA外设ADC基地址
	DMA_InitStructure.DMA_MemoryBaseAddr = (u32)&AD_Value;  //DMA内存基地址
	DMA_InitStructure.DMA_DIR = DMA_DIR_PeripheralSRC;  //内存作为数据传输的目的地
	DMA_InitStructure.DMA_BufferSize = ADC_TIMES*ADC_CHANALS;  //DMA通道的DMA缓存的大小
	DMA_InitStructure.DMA_PeripheralInc = DMA_PeripheralInc_Disable;  //外设地址寄存器不变
	DMA_InitStructure.DMA_MemoryInc = DMA_MemoryInc_Enable;  //内存地址寄存器递增
	DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_HalfWord;  //数据宽度为16位
	DMA_InitStructure.DMA_MemoryDataSize = DMA_MemoryDataSize_HalfWord; //数据宽度为16位
	DMA_InitStructure.DMA_Mode = DMA_Mode_Circular;  //工作在循环缓存模式
	DMA_InitStructure.DMA_Priority = DMA_Priority_High; //DMA通道 x拥有高优先级 
	DMA_InitStructure.DMA_M2M = DMA_M2M_Disable;  //DMA通道x没有设置为内存到内存传输
	DMA_Init(DMA1_Channel1, &DMA_InitStructure);  //根据DMA_InitStruct中指定的参数初始化DMA的通道
}

void BP_ADC_INIT(void)
{
	ADC_InitTypeDef ADC_InitStructure;  

	RCC_APB2PeriphClockCmd(RCC_APB2Periph_ADC1	, ENABLE );	  //使能ADC1通道时钟

	/* Configure ADCCLK such as ADCCLK = PCLK2/6 */ 
	RCC_ADCCLKConfig(RCC_PCLK2_Div6);   //72M/6=12,ADC最大时间不能超过14M

	//PC   0/ 1/ 2/ 3/ 4/ 5  pb0 1 作为模拟通道输入引脚 
	//adc  10 11 12 13 14 15  8  9      
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_0|GPIO_Pin_1|GPIO_Pin_2|GPIO_Pin_3|GPIO_Pin_4|GPIO_Pin_5;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AIN;		//模拟输入引脚
	GPIO_Init(GPIOC, &GPIO_InitStructure);

	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_0|GPIO_Pin_1;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AIN;		//模拟输入引脚
	GPIO_Init(GPIOB, &GPIO_InitStructure);
	/* Resets ADC1 */ 
	ADC_DeInit(ADC1);  //将外设 ADC1 的全部寄存器重设为缺省值
	
	/* ADC1 configuration ------------------------------------------------------*/
	ADC_InitStructure.ADC_Mode = ADC_Mode_Independent;	//ADC工作模式:ADC1和ADC2工作在独立模式
	ADC_InitStructure.ADC_ScanConvMode = ENABLE; 		//ENABLE	模数转换工作在扫描模式
												 		//DISABLE 	模数转换工作在单次模式
	ADC_InitStructure.ADC_ContinuousConvMode = ENABLE;	//ENABLE	模数转换工作在连续转换模式
														//DISABLE	模数转换工作在单次转换模式ENABLE
	ADC_InitStructure.ADC_ExternalTrigConv = ADC_ExternalTrigConv_None;	//转换由软件而不是外部触发启动
	ADC_InitStructure.ADC_DataAlign = ADC_DataAlign_Right;	//ADC数据右对齐
	ADC_InitStructure.ADC_NbrOfChannel = ADC_CHANALS;	//顺序进行规则转换的ADC通道的数目
	ADC_Init(ADC1, &ADC_InitStructure);	//根据ADC_InitStruct中指定的参数初始化外设ADCx的寄存器
	
	/* ADC1 regular channel11 configuration */ 
	//设置指定ADC的规则组通道，设置它们的转化顺序和采样时间
	//ADC1,ADC通道x,规则采样顺序值为y,采样时间为239.5周期
	ADC_RegularChannelConfig(ADC1, ADC_Channel_8, 1, ADC_SampleTime_239Cycles5 );
	ADC_RegularChannelConfig(ADC1, ADC_Channel_9, 2, ADC_SampleTime_239Cycles5 );
	ADC_RegularChannelConfig(ADC1, ADC_Channel_10, 3, ADC_SampleTime_239Cycles5 );
	ADC_RegularChannelConfig(ADC1, ADC_Channel_11, 4, ADC_SampleTime_239Cycles5 );
	ADC_RegularChannelConfig(ADC1, ADC_Channel_12, 5, ADC_SampleTime_239Cycles5 );
	ADC_RegularChannelConfig(ADC1, ADC_Channel_13, 6, ADC_SampleTime_239Cycles5 );
	ADC_RegularChannelConfig(ADC1, ADC_Channel_14, 7, ADC_SampleTime_239Cycles5 );
	ADC_RegularChannelConfig(ADC1, ADC_Channel_15, 8, ADC_SampleTime_239Cycles5 );
	
	 // 开启ADC的DMA支持（要实现DMA功能，还需独立配置DMA通道等参数）
    ADC_DMACmd(ADC1, ENABLE);	
	
	/* Enable ADC1 */
	ADC_Cmd(ADC1, ENABLE);	//使能指定的ADC1
	
	
	/* Enable ADC1 reset calibaration register */   
	ADC_ResetCalibration(ADC1);	//重置指定的ADC1的校准寄存器
	/* Check the end of ADC1 reset calibration register */
	while(ADC_GetResetCalibrationStatus(ADC1));	//获取ADC1重置校准寄存器的状态,设置状态则等待
	
	/* Start ADC1 calibaration */
	ADC_StartCalibration(ADC1);		//开始校准
	/* Check the end of ADC1 calibration */
	while(ADC_GetCalibrationStatus(ADC1));		//等待校准完成
	
	/* Start ADC1 Software Conversion */ 
	//ADC_SoftwareStartConvCmd(ADC1, ENABLE);		//使能指定的ADC1的软件转换启动功能
}

//==============================================================================
//Adc15DmaConfig:ADC通道转换的DMA配置 
//DMA_CHx：DMA通道
//cpar：数据来源，ADC的DR寄存器
//cmar: 数据去向，内存地址
//cndtr: 长度 
//返回值  :无   
//==============================================================================
void Adc15DmaConfig(DMA_Channel_TypeDef* DMA_CHx,u32 cpar,u32 cmar,u16 cndtr)
{

	DMA_InitTypeDef DMA_InitStructure;
 	RCC_AHBPeriphClockCmd(RCC_AHBPeriph_DMA1|RCC_AHBPeriph_DMA2, ENABLE);	//使能DMA传输	   |RCC_AHBPeriph_DMA2
	
    DMA_DeInit(DMA_CHx);   //将DMA的通道1寄存器重设为缺省值
	DMA_InitStructure.DMA_PeripheralBaseAddr = cpar;                     //DMA外设ADC基地址
	DMA_InitStructure.DMA_MemoryBaseAddr = cmar;                         //DMA内存基地址
	DMA_InitStructure.DMA_DIR = DMA_DIR_PeripheralSRC;                   //内存作为数据传输的目的地
	DMA_InitStructure.DMA_BufferSize = cndtr;                                    //DMA通道的DMA缓存的大小
	DMA_InitStructure.DMA_PeripheralInc = DMA_PeripheralInc_Disable;             //外设地址寄存器不变
	DMA_InitStructure.DMA_MemoryInc = DMA_MemoryInc_Enable;  					 //内存地址寄存器递增
	DMA_InitStructure.DMA_PeripheralDataSize = DMA_PeripheralDataSize_HalfWord;  //外设数据宽度为16位
	DMA_InitStructure.DMA_MemoryDataSize = DMA_MemoryDataSize_HalfWord;          //内存数据宽度为16位
	DMA_InitStructure.DMA_Mode = DMA_Mode_Circular;  		//工作在正常缓存模式	
	DMA_InitStructure.DMA_Priority = DMA_Priority_VeryHigh; //DMA通道高优先级 
	DMA_InitStructure.DMA_M2M = DMA_M2M_Disable;  //DMA通道x没有设置为内存到内存传输
	DMA_Init(DMA_CHx, &DMA_InitStructure);  //根据DMA_InitStruct中指定的参数初始化DMA的通道USART1_Tx_DMA_Channel所标识的寄存器	  	
}		
 u16 Get_Adc(u8 ch)  //单次模式 
	{

	ADC_RegularChannelConfig(ADC1, ch, 1, ADC_SampleTime_239Cycles5 );	//ADC1,ADC通道3,规则采样顺序值为1,采样时间为239.5周期	  			    

	/* Start ADC1 Software Conversion */ 
	ADC_SoftwareStartConvCmd(ADC1, ENABLE);		//使能指定的ADC1的软件转换启动功能
	 
	while(!ADC_GetFlagStatus(ADC1, ADC_FLAG_EOC ));//等待转换结束
	return ADC_GetConversionValue(ADC1);	//返回最近一次ADC1规则组的转换结果
	}

