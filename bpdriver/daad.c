 void AD_filter(void)
{
  	int  sum = 0;
	u8  count,i;    
  	for(i=0;i<ADC_CHANALS;i++)

      {

         for ( count=0;count<ADC_TIMES;count++)

          {

           		sum += AD_Value[count][i];

          }

          After_filter[i]=(u16)sum/ADC_TIMES;

          sum=0;
      }

 }



